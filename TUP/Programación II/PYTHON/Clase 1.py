# Ejercicio 1: Iterar un rango de 0 a 10 e imprimir números divisibles entre 3
# Ejemplo de ejecución: 0,3,6,9
print ("Ejercicio 1")
for x in range (0,11):
    if x % 3 ==0:
        print (x)

